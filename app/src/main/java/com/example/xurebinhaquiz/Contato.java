    public int getPorcentagemC() {
        return porcentagemC;
    }

    public void setPorcentagemC(int porcentagemC) {
        this.porcentagemC = porcentagemC;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
